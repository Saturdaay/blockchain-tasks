# task1_hash.py

import hashlib  # Хеш-функцияларымен жұмыс істеу үшін hashlib кітапханасын қосамыз

# Хеш-функция
def simple_hash(data):
    """Бұл функция деректерді (строканы) қабылдап, оның хешін есептейді. 
    Хеш - бұл деректердің қысқартылған әрі бірегей коды. Мұнда MD5 алгоритмі пайдаланылады."""
    
    # Хеш есептеу: алдымен деректерді байттар түрінде кодтаймыз, сосын MD5 хешін есептейміз
    return hashlib.md5(data.encode('utf-8')).hexdigest()  # encode('utf-8') - строканы байттарға айналдыру

# Пример деректерді хештеу
if __name__ == "__main__":
    data = "Hello, Blockchain!"  # Хештеу үшін деректерді аламыз
    hashed_data = simple_hash(data)  # Деректердің хешін есептейміз
    print(f"Original data: {data}")  # Алғашқы деректерді көрсетеміз
    print(f"Hashed data: {hashed_data}")  # Хештелген деректерді көрсетеміз
